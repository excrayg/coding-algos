# 2D Problems

# Suppose we are given a 2 dimensional array of integer values >= 0.
# The 2d array represents an island, where the integer value at each index represents the height of terrain at that location. We assume that ocean surrounds the island on all 4 sides. What is the highest point on the island where rain could collect and drain into the ocean? 

# Example 1:

# 1111
# 1311
# 1211

# Rain would drain from position (1,1) 

# Example 2:

# 222221
# 211121
# 213121
# 211121
# 222221

# In this case, 3 is the highest point, but it has a valley around it, so it doesn't ever reach the ocean. 

